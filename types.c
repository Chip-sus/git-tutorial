#include <stdio.h>

int main() {
    // Integers hehe more hehehe
    int positive = 10;
    int negative = -10;
    unsigned int positiveOnly = 20;

    printf("Positive integer: %d\n", positive);
    printf("Negative integer: %d\n", negative);
    printf("Unsigned integer: %u\n", positiveOnly);

    // Floating-point numbers
    float myFloat = 3.14159265358979323846f;
    double myDouble = 3.14159265358979323846;

    printf("Float: %.15f\n", myFloat);
    printf("Double: %.15lf\n", myDouble);

    return 0;
}
